/**
 * 【なぜこのコードを学ぶのか】
 * プログラムはどこから動き始めるのか？ main メソッドこそがその答えです。
 * Javaを書くうえで「呪文」のように丸暗記しがちな main の宣言ですが、
 * 各キーワードには明確な意味があります。意味を知ると、
 * なぜ少しでも書き方が違うと動かないのかが理解できます。
 */
package com.example.introduction;

public class Hello {
    //                         ↓ ここから main メソッドの宣言
    public static void main(String[] args) {
    // ↑      ↑     ↑         ↑
    // │      │     │         └─ コマンドライン引数（起動時に外から渡せる文字列の配列）
    // │      │     └─────────── 戻り値なし（何も返さないメソッド）
    // │      └───────────────── プログラム起動時にオブジェクトを作らずに呼べる
    // └──────────────────────── どこからでも呼び出せる（アクセス制限なし）

        // System.out.println は「標準出力に1行表示して改行する」命令です
        System.out.println("Hello, Java!");
        System.out.println("はじめてのプログラム");
    }
}
