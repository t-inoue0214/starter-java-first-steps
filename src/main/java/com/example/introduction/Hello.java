/**
 * 【なぜこのコードを学ぶのか】
 * プロのエンジニアは、新しい言語や環境に触れるとき、必ず「Hello World」から始めます。
 * その理由は「環境が正しく動いているかを最初に確認するため」です。
 *
 * もし最初から複雑なプログラムを書いて動かなかった場合、原因が
 * 「コードのバグなのか」「環境のセットアップが壊れているのか」を区別できません。
 * Hello World が動けば「環境は正常」と確定できます。これは現場でも行われているエンジニアリングの習慣です。
 *
 * 第01章の目標はシンプルに「動かすこと」だけです。
 * main 宣言の各キーワード（public・static・void など）の意味は第04章で改めて学びます。
 * 今は「各キーワードに意味がある」ことだけ頭の隅に置いておいてください。
 * 第04章を終えてこのファイルに戻ってくると、全部理解できます。
 */

// package はこのファイルの「住所」です。
// クラス名の衝突（同名クラスが別の場所に存在してしまう問題）を防ぐためにあります。
// 詳細は第04章で学びます。今は「おまじない」として読み飛ばして構いません。
package com.example.introduction;

// クラスについては第04章で詳しく学びます。今は「プログラムを入れる箱」とだけ覚えてください。
public class Hello {

    public static void main(String[] args) {
        // public  = どこからでも呼び出せる（詳細は第04章）
        // static  = クラスそのものに属するという指定（詳細は第04章）
        // void    = 戻り値なし（何も返さないメソッド）
        // main    = Javaプログラムがここから動き始める、特別な名前
        // String[] args = コマンドライン引数（起動時に外から渡せる文字列の配列）
        //               ※「配列」は第03章で学びます。今は「おまじない」として覚えておけば大丈夫です。

        // System.out.println は「標準出力に1行表示して改行する」命令です。
        // 標準出力 = 通常はターミナルの画面のことです。
        // 現場では「プログラムが今どこまで動いているかを確認する」ために頻繁に使います。
        // いわばプログラムの「話し声」のようなもので、デバッグの第一歩として欠かせません。
        System.out.println("Hello, Java!");
        System.out.println("はじめてのプログラム");

        // ここより下に試したいコードを追加してください
    }
}
