/**
 * 【なぜこのコードを学ぶのか】
 * フィールドを public のまま外部から直接変更できる設計では、
 * 「age = -100」のような不正な値を誰でも書き込めてしまいます。
 * private フィールド＋コンストラクタ＋ゲッターという組み合わせで
 * データを守る（カプセル化）ことが、現場での基本的な設計です。
 * Before/After の対比でその違いを体験してください。
 */
package com.example.class_and_objects;

public class Main {
    public static void main(String[] args) {

        // ========== Step 0: 手続き型でデータを管理する限界 ==========
        // クラスを使わずに、バラバラな変数で犬の情報を管理してみる
        String dog1Name = "ポチ";
        int    dog1Age  = 3;
        String dog2Name = "ハチ";
        int    dog2Age  = 5;

        System.out.println("=== 変数バラバラ管理の問題 ===");
        System.out.println(dog1Name + " は " + dog1Age + " 歳");
        System.out.println(dog2Name + " は " + dog2Age + " 歳");
        // 犬が増えるたびに変数が2つずつ増える。犬が10匹なら変数20個、100匹なら200個。
        // さらに「dog2Name と dog2Age が同じ犬のデータ」という保証がコードにない。
        // → この限界を解決するのがクラス（データと振る舞いをひとまとめにする道具）

        // ========== Before: public フィールドを直接触る問題のある書き方 ==========
        System.out.println("=== Before: public フィールド ===");

        DogBefore dog1 = new DogBefore();
        dog1.name = "ポチ";
        dog1.age = 3;
        dog1.introduce();

        // 問題点: 不正な値を何でも書き込める！
        // -100歳の犬は現実に存在しないが、コンパイルエラーにならない
        dog1.age = -100;
        System.out.println("不正な値を書き込んでしまった:");
        dog1.introduce(); // -100歳の犬が表示されてしまう

        // ========== After: private フィールド＋コンストラクタ＋ゲッターの正しい書き方 ==========
        System.out.println("\n=== After: private フィールド ===");

        // コンストラクタで名前と年齢をセットしてオブジェクトを作る
        Dog dog2 = new Dog("ハチ", 5);
        System.out.println(dog2.getDescription()); // 戻り値ありメソッドで情報を取得
        System.out.println("年齢: " + dog2.getAge() + "歳"); // ゲッターで年齢を取得

        // dog2.age = -100; // private なのでコンパイルエラーになる！
        // → 外部から不正な値を書き込む手段がなく、データが守られている

        Dog dog3 = new Dog("コロ", 2);
        dog3.bark();

        // ----------------------------------------
        // ここで学んだ「new Dog("ハチ", 5)」と同じ仕組みで、
        // 第05章では「new Scanner(System.in)」「new ArrayList<>()」を使う。
        // Java 標準クラスも、あなたが作った Dog クラスも、
        // 「設計図（クラス）から実体（インスタンス）を作る」という同じ仕組みで動いている。
        // ----------------------------------------
    }

    // ---------------------------------------------------------
    // Before: 問題のある設計（public フィールドを直接触れる「犬の設計図」）
    // ---------------------------------------------------------
    public static class DogBefore {
        // フィールドが public なので、外部から自由に書き換えられてしまう
        public String name;
        public int age;

        public void introduce() {
            System.out.println("私の名前は" + name + "です。" + age + "歳です。");
        }
    }

    // ---------------------------------------------------------
    // After: 改善した設計（private フィールドでデータを守る「犬の設計図」）
    // ---------------------------------------------------------
    public static class Dog {
        // private にすることで、このクラスの外からは直接触れない
        private String name;
        private int age;

        // ---------------------------------------------------------
        // コンストラクタ: オブジェクト生成時に必ず呼ばれる初期化の処理
        // new Dog("ハチ", 5) と書くと、ここが実行される
        // ---------------------------------------------------------
        public Dog(String name, int age) {
            // this.name は「このオブジェクト自身の name フィールド」を指す
            // 引数の name と区別するために this. が必要
            this.name = name;
            this.age = age;
        }

        // ---------------------------------------------------------
        // ゲッター: private フィールドの値を外部に「読み取り専用」で公開する
        // ---------------------------------------------------------
        public int getAge() {
            return age; // age の値を返す（書き換えはできない）
        }

        // 【設計ポイント: なぜ setAge() がないのか？】
        // 値を後から書き換えるメソッド（setter）は意図的に作っていない。
        // 「なぜ setter を書かないのか」の答えは第08章のイミュータブル設計で学ぶ。

        // ---------------------------------------------------------
        // 戻り値ありメソッド: 文字列を組み立てて返す
        // ---------------------------------------------------------
        public String getDescription() {
            return name + "は" + age + "歳の犬です。";
        }

        // ---------------------------------------------------------
        // 通常のメソッド: 動作を表す
        // ---------------------------------------------------------
        public void bark() {
            System.out.println(name + "が吠えました：ワンワン！");
        }
    }
}
