/**
 * 【なぜこのコードを学ぶのか】
 * 条件分岐はプログラムの「判断」を担う最重要構文です。
 * 旧来の switch 文は「break 書き忘れで次のケースに落ちるバグ」が頻発していましたが、
 * Java 14以降の switch 式（`->` 構文）はその問題を構造的に解決します。
 * なぜモダンな書き方に移行すべきかを、旧来との対比で体験します。
 */
package com.example.control_flow;

public class Condition {
    public static void main(String[] args) {
        int score = 85;

        // ---------------------------------------------------------
        // 1. 基本的なif文
        // ---------------------------------------------------------
        // カッコの中身は必ず「true」か「false」になる式でなければいけません
        if (score >= 60) {
            System.out.println("合格です！");
        } else {
            System.out.println("不合格...");
        }

        // ---------------------------------------------------------
        // 2. 複数の条件 ( && は「かつ」、 || は「または」)
        // ---------------------------------------------------------
        if (score >= 80 && score < 90) {
            System.out.println("あと少しで90点！惜しい！");
        }

        // 【重要】 = と == を混同しないようにしましょう
        // = は「代入」（右の値を左の変数に入れる）
        // == は「比較」（左と右が等しいか確かめる）
        //
        // 間違い例: if (score = 60) → Javaではコンパイルエラーになります
        // 正しい例: if (score == 60) → 「scoreが60と等しいか？」という比較になります
        if (score == 85) {
            System.out.println("ちょうど85点！");
        }

        // 【実験】 他の言語（JavaScriptなど）との違い
        // Javaでは、数字や文字列をそのまま条件にすることはできません。
        // 以下のコメントを外すとエラーになります。
        // if (1) { System.out.println("これはエラーになります"); }

        // ---------------------------------------------------------
        // 3. switch式（Java 14以降・Java 21で正式採用）
        // ---------------------------------------------------------

        // ========== Before: 旧来の switch 文（Java 13以前の書き方） ==========
        // case の末尾に break を書き忘れると、次の case に「落ちて」しまい
        // 意図しない処理が続いて実行されるバグ（フォールスルー）が起きていました。
        //
        // switch (score / 10) {
        //     case 10:
        //     case 9:  System.out.println("S"); break; // break を1つ消したら...
        //     case 8:  System.out.println("A"); break; // ...ここも実行されてしまう！
        //     case 7:  System.out.println("B"); break;
        //     case 6:  System.out.println("C"); break;
        //     default: System.out.println("D");
        // }

        // ========== After: switch 式（Java 14以降） ==========
        // -> 構文ではフォールスルーが起きない構造になっており、
        // break も不要で、値を直接返せます。
        // ※ switch式は代入文の一部なので、末尾にセミコロン「;」が必要です。
        String grade = switch (score / 10) { // score を 10 で割って段階を判定
            case 10, 9 -> "S"; // 90〜100点
            case 8     -> "A"; // 80〜89点
            case 7     -> "B"; // 70〜79点
            case 6     -> "C"; // 60〜69点
            default    -> "D"; // 59点以下
        }; // switch式は代入文の一部として扱われるため、末尾にセミコロンが必要
        System.out.println("成績: " + grade); // score=85 なので "A" が表示される

        // ---------------------------------------------------------
        // 【アンチパターン集】やってはいけない条件分岐の書き方
        // ---------------------------------------------------------

        // NG例1: = と == の混同（コンパイルエラーになります）
        // 以下のコメントを外すと "incompatible types: int cannot be converted to boolean" が出ます。
        // if 文のカッコ内に「代入の結果（整数）」を渡しているため、Java はエラーを出します。
        // if (score = 60) {
        //     System.out.println("これはエラーになります");
        // }
        // → 正しくは: if (score == 60)

        // NG例2: String の比較に == を使う（コンパイルは通るが、バグになることがある）
        // == は「同じオブジェクトかどうか（場所が同じか）」を比較するもので、
        // 「中身の文字が同じか」を確かめるには .equals() を使います。
        String input = new String("hello"); // わざと new で別のオブジェクトとして作成
        // NG: if (input == "hello") → false になることがある！（コンパイルは通るが動作がおかしい）
        // OK:
        if (input.equals("hello")) {
            System.out.println("文字列の比較には .equals() を使います（第02章の復習）");
        }
        // 【現場では】 == で String を比較するバグは定番ミスです。
        // 常に .equals() を使う習慣をつけましょう。
    }
}
