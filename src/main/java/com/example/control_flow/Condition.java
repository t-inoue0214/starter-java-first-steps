/**
 * 【なぜこのコードを学ぶのか】
 * 条件分岐はプログラムの「判断」を担う最重要構文です。
 * 旧来の switch 文は「break 書き忘れで次のケースに落ちるバグ」が頻発していましたが、
 * Java 14以降の switch 式（`->` 構文）はその問題を構造的に解決します。
 * なぜモダンな書き方に移行すべきかを、旧来との対比で体験します。
 */
package com.example.control_flow;

public class Condition {
    public static void main(String[] args) {
        int score = 85;

        // ---------------------------------------------------------
        // 1. 基本的なif文
        // ---------------------------------------------------------
        // カッコの中身は必ず「true」か「false」になる式でなければいけません
        if (score >= 60) {
            System.out.println("合格です！");
        } else {
            System.out.println("不合格...");
        }

        // ---------------------------------------------------------
        // 2. 複数の条件 ( && は「かつ」、 || は「または」)
        // ---------------------------------------------------------
        if (score >= 80 && score < 90) {
            System.out.println("あと少しで90点！惜しい！");
        }

        // 【実験】 他の言語（JavaScriptなど）との違い
        // Javaでは、数字や文字列をそのまま条件にすることはできません。
        // 以下のコメントを外すとエラーになります。

        // if (1) { System.out.println("これはエラーになります"); }

        // ---------------------------------------------------------
        // 3. switch式（Java 14以降・Java 21で正式採用）
        // ---------------------------------------------------------
        // 旧来の switch 文では各 case の末尾に break を書き忘れると
        // 次の case に「落ちて（フォールスルーして）」バグになっていました。
        // -> 構文の switch 式はフォールスルーが起きない構造になっており、
        // 短く書けてバグも出にくい、現代の Javaらしい書き方です。
        var grade = switch (score / 10) { // score を 10 で割って段階を判定
            case 10, 9 -> "S"; // 90〜100点
            case 8     -> "A"; // 80〜89点
            case 7     -> "B"; // 70〜79点
            case 6     -> "C"; // 60〜69点
            default    -> "D"; // 59点以下
        }; // switch式は文ではなく「値を返す式」なので末尾にセミコロンが必要
        System.out.println("成績: " + grade); // score=85 なので "A" が表示される

        // ---------------------------------------------------------
        // 4. while文（回数が決まっていないループ）
        // ---------------------------------------------------------
        // for文は「あらかじめ繰り返し回数が決まっているとき」に向いています。
        // while文は「ある条件を満たす間ずっと繰り返す」ときに向いています。
        // 例: ゲームのメインループ（プレイヤーが終了するまでずっと動かす）
        int countdown = 3; // カウントダウン開始の数字
        System.out.println("--- カウントダウン開始 ---");
        // countdown が 0 より大きい間、繰り返す
        while (countdown > 0) {
            System.out.println(countdown + "...");
            countdown--; // 1ずつ減らす（これを忘れると永遠に終わらない無限ループになる！）
        }
        System.out.println("発射！");
    }
}
