public class Main {
    public static void main(String[] args) {
        // ---------------------
        // 1. 設計図から実体（オブジェクト）を作る
        // 「Dog型の変数 pochi に、新しい Dog を作って入れる」
        // ---------------------
        Dog dog1 = new Dog();

        // ---------------------
        // 2. データを入れる
        // 「.」は「〜の」という意味です
        // ---------------------
        dog1.name = "ポチ";
        dog1.age = 3;

        // ---------------------
        // 3. 別の犬も作れます（設計図は使い回せる！）
        // ---------------------
        Dog dog2 = new Dog();
        dog2.name = "ハチ";
        dog2.age = 5;

        // ---------------------
        // 4. メソッドを使う（命令する）
        // ---------------------
        System.out.println("--- ポチの動作 ---");
        dog1.bark();
        dog1.introduce();

        System.out.println("--- ハチの動作 ---");
        dog2.bark(); // ハチの名前で吠えます
    }

    // ---------------------
    // これは「犬の設計図」です
    // ---------------------
    public static class Dog {
        // ---------------------
        // 1. フィールド（属性・データ）
        // 犬が持つデータ
        // ---------------------
        String name;
        int age;

        // ---------------------
        // 2. メソッド（操作・振る舞い）
        // 犬ができること
        // ---------------------
        void bark() {
            System.out.println(name + "が吠えました：ワンワン！");
        }

        void introduce() {
            System.out.println("私の名前は" + name + "です。" + age + "歳です。");
        }
    }
}